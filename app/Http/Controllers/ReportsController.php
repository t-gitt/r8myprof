     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $pid)
    {
        $this->validate($request, ['report' => 'required']);

        $report = new Report;
        $report->prof_id = $request->get('prof_id');
        $report->student_id = auth()->user()->id;
        $report->report = $request->input('report');
        $report->save();

        return redirect('/professors')->with('success', 'Thank you! we will look into your report and take the suitable actions.');

    }

